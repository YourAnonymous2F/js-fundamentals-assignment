// Simple Calculator
// Task: Simple calculator that performs basic arithmetic operations on two numbers

// declaring variables for the two numbers
let num1 = 10;
let num2 = 4;

// ADDITION
console.log (num1 + num2);

// SUBTRACTION
console.log (num1 - num2);

// MULTIPLICATION
console.log (num1 * num2);

// DIVISION
console.log (num1 / num2);

// MODULUS (REMINDER)
console.log (num1 % num2);

// EXPONENTIATION
console.log (num1 ** num2);